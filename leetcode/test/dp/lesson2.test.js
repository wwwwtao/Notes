import cheap from '../../code/dp/lesson2'

test('0,2,0', () => {
  expect(cheap([
    [0, 1, 100],
    [1, 2, 100],
    [0, 2, 500]
  ], 0, 2, 0)).toBe(500)
  expect(cheap([[0, 1, 100], [1, 2, 100], [0, 2, 500]], 0, 2, 1)).toBe(200)
  expect(cheap([[4, 1, 1], [1, 2, 3], [0, 3, 2], [0, 4, 10], [3, 1, 1], [1, 4, 3]], 2, 1, 1)).toBe(-1)
})
